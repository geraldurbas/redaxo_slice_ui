<b>Slice Footer</b>

<br /><br />
Slices bekommen im Backend einen Footer für die Ausgabe. In diesem Footer können Icons/Funktionen definiert werden. Die vom Addon mitgelieferte Standardfunktion fügt ein Löschen-Icon hinzu, welches einen Slice als "Löschbar" markiert. Nach der Markierung kann man über und unter den Slices auf <b>Gewählte Löschen</b> klicken und die gewählten Slices werden entfernt ohne die Seite neu zu laden.
<br><br><br>
<b>Alle Löschen</b>
<br><br>
Das Addon liefert den Button <b>Alles Löschen</b> mit dem alle Slices eines Artikels gelöscht werden können.  
