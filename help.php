<b>Slice UI</b>

<br><br>
Slice UI erweitert Slices um weitere Funktionen wie kopieren, ausschneiden und aktivieren bzw. deaktivieren.<br>
Das Addon erweitert die Standardfunktionen um AJAX-Aufrufe und bietet einige nützliche Gadgets, mit denen der Content erheblich einfacher bearbeitet werden kann.
<br><br><br>
<b>Features</b>
<br><br>

<ul>
  <li>Move Up/Down ohne reload</li>
  <li>Online/Offline ohne reload</li>
  <li>Slices sortieren mit Drag&Drop</li>
  <li>Online von / bis</li>
  <li>Alle Slices löschen / Gewählte Slices löschen</li>
  <li>Slice-Buttons scrollen mit</li>
  <li>Slices kopieren</li>
  <li>Slices ausschneiden</li>
  <li>Fast alles optional</li>
  <li>Auf bestimmte Module beschränken</li>
  <li>Einige neue Extension Points für Entwickler</li>
</ul>